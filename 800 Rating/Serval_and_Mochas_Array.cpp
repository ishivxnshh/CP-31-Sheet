#define ll long long
#define f(i, n) for (ll i = 0; i < n; i++)
#define ia(a, n) \
    ll a[n];     \
    f(i, n) cin >> a[i]
#define iv(v, n)     \
    vector<ll> v(n); \
    f(i, n) cin >> v[i]
#define nline '\n'
#define yes cout << "YES\n"
#define no cout << "NO\n"
void solve()
{
    ll n;
    cin >> n;
    iv(v, n);
    string res = "no";
    f(i, n)
    {
        for (int j = i + 1; j < n; j++)
        {
            if (__gcd(v[i], v[j]) <= 2)
            {
                res = "yes"; break;
            }
        }
    }
    cout << res << nline;
}

